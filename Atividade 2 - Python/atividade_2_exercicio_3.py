# Calculadora Simples
numero1 = input("Escolha o primeiro número.")
numero2 = input("Escolha o segundo número.")
# Os dois números serão selecionados pelo usuário.
soma = int(numero1) + int(numero2)
print("A soma dos números é " +str(soma) + ".") # Cálculo de soma.

subtracao = int(numero1) - int(numero2) # Cálculo de subtração.
print("A subtração dos números é " +str(subtracao) + ".")

multiplicacao = int(numero1) * int(numero2) # Cálculo de multiplicação.
print("A multiplicação dos números é " +str(multiplicacao) + ".")

divisao = int(numero1) / int(numero2) # Cálculo de divisão.
print("A divisão dos números é " +str(divisao) + ".")